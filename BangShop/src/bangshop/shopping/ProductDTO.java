package bangshop.shopping;

public class ProductDTO {

	private int proNo;
	private String image;
	private String category;
	private String place;
	private String proStatus;
	private String title;
	private String price;
	private String deliPay;
	private String changeOk;
	private String proCount;
	private String proComment;
	private String tag;

	public int getProNo() {
		return proNo;
	}

	public void setProNo(int proNo) {
		this.proNo = proNo;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

	public String getProStatus() {
		return proStatus;
	}

	public void setProStatus(String proStatus) {
		this.proStatus = proStatus;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getDeliPay() {
		return deliPay;
	}

	public void setDeliPay(String deliPay) {
		this.deliPay = deliPay;
	}

	public String getChangeOk() {
		return changeOk;
	}

	public void setChangeOk(String changeOk) {
		this.changeOk = changeOk;
	}

	public String getProCount() {
		return proCount;
	}

	public void setProCount(String proCount) {
		this.proCount = proCount;
	}

	public String getProComment() {
		return proComment;
	}

	public void setProComment(String proComment) {
		this.proComment = proComment;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

}
